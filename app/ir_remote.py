class IrRemote:
    def __init__(self):
        self._codes = {
            (255, 0, 93, 162): "1",
            (255, 0, 157, 98): "2",
            (255, 0, 29, 226): "3",
            (255, 0, 221, 34): "4",
            (255, 0, 253, 2): "5",
            (255, 0, 61, 194): "6",
            (255, 0, 31, 224): "7",
            (255, 0, 87, 168): "8",
            (255, 0, 111, 144): "9",
            (255, 0, 103, 152): "0",
            (255, 0, 231, 24): "up",
            (255, 0, 165, 90): "right",
            (255, 0, 181, 74): "down",
            (255, 0, 239, 16): "left",
            (255, 0, 199, 56): "ok",
            (255, 0, 151, 104): "*",
            (255, 0, 79, 176): "#",
        }

    def decode(self, code):
        if code in self._codes:
            return self._codes[code]
        return None
